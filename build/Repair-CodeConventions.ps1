param(
    [string]$RootDirectory = (Get-Location).Path
)

$ErrorActionPreference = "Stop"

function Get-TextFromCodes {
    param(
        [int[]]$Codes
    )

    return ([char[]]$Codes) -join ""
}

function Read-FileTextUtf8 {
    param(
        [string]$FilePath
    )

    return [System.IO.File]::ReadAllText($FilePath, [System.Text.Encoding]::UTF8)
}

function Write-FileTextUtf8 {
    param(
        [string]$FilePath,
        [string]$FileText
    )

    Set-Content -LiteralPath $FilePath -Value $FileText -Encoding UTF8
}

function Get-SourceFiles {
    param(
        [string]$RootPath
    )

    $candidateFiles = Get-ChildItem -Path $RootPath -Recurse -File -Filter "*.cs"

    foreach ($candidateFile in $candidateFiles) {
        if ($candidateFile.FullName -match "[\\/](bin|obj)[\\/]") {
            continue
        }

        if ($candidateFile.Name -match "(\.Designer|\.g|\.g\.i)\.cs$") {
            continue
        }

        if ($candidateFile.Name -eq "AssemblyInfo.cs") {
            continue
        }

        $fileContent = Read-FileTextUtf8 -FilePath $candidateFile.FullName
        if ($fileContent -match "<auto-generated") {
            continue
        }

        $candidateFile.FullName
    }
}

function Remove-FullLineComments {
    param(
        [string[]]$Lines
    )

    $result = New-Object System.Collections.Generic.List[string]
    $insideBlockComment = $false

    foreach ($line in $Lines) {
        $trimmedLine = $line.Trim()

        if ($insideBlockComment) {
            if ($trimmedLine -match "\*/") {
                $insideBlockComment = $false
            }

            continue
        }

        if ($trimmedLine.StartsWith("///", [System.StringComparison]::Ordinal)) {
            continue
        }

        if ($trimmedLine.StartsWith("//", [System.StringComparison]::Ordinal)) {
            continue
        }

        if ($trimmedLine.StartsWith("/*", [System.StringComparison]::Ordinal)) {
            if ($trimmedLine -notmatch "\*/") {
                $insideBlockComment = $true
            }

            continue
        }

        $result.Add($line)
    }

    return $result
}

function Get-LeadingWhitespace {
    param(
        [string]$LineText
    )

    $match = [regex]::Match($LineText, "^\s*")
    return $match.Value
}

function Get-BraceDelta {
    param(
        [string]$LineText
    )

    $openCount = ([regex]::Matches($LineText, "\{")).Count
    $closeCount = ([regex]::Matches($LineText, "\}")).Count
    return $openCount - $closeCount
}

function Test-TypeDeclaration {
    param(
        [string]$LineText
    )

    $trimmedLine = $LineText.Trim()
    if ([string]::IsNullOrWhiteSpace($trimmedLine)) {
        return $false
    }

    if ($trimmedLine.StartsWith("[", [System.StringComparison]::Ordinal)) {
        return $false
    }

    return $trimmedLine -cmatch "^(public|protected|internal|private|file|sealed|abstract|static|partial|readonly|\s)*\s*(class|interface|struct|enum)\s+\w+" -or
        $trimmedLine -cmatch "^(public|protected|internal|private|file|sealed|abstract|static|partial|readonly|\s)*\s*record(\s+(class|struct))?\s+\w+"
}

function Test-PropertyDeclaration {
    param(
        [string]$LineText
    )

    $trimmedLine = $LineText.Trim()
    if ([string]::IsNullOrWhiteSpace($trimmedLine)) {
        return $false
    }

    if ($trimmedLine.StartsWith("[", [System.StringComparison]::Ordinal)) {
        return $false
    }

    if ($trimmedLine -cmatch "^(if|for|foreach|while|switch|catch|lock|using|return|throw|await|var|new|this|base)\b") {
        return $false
    }

    if ($trimmedLine -match "\(") {
        return $false
    }

    if ($trimmedLine -cmatch "\b(class|interface|record|struct|enum|namespace|using)\b") {
        return $false
    }

    return $trimmedLine -match "\{\s*(get|set|init)\s*;" -or $trimmedLine -match "=>"
}

function Test-FieldDeclaration {
    param(
        [string]$LineText
    )

    $trimmedLine = $LineText.Trim()
    if ([string]::IsNullOrWhiteSpace($trimmedLine)) {
        return $false
    }

    if ($trimmedLine.StartsWith("[", [System.StringComparison]::Ordinal)) {
        return $false
    }

    if ($trimmedLine -match "\(" -or $trimmedLine -match "\{") {
        return $false
    }

    if ($trimmedLine -match "\b(namespace|using|return|throw|break|continue)\b") {
        return $false
    }

    if (-not ($trimmedLine -cmatch "^(public|protected|internal|private|file|static|readonly|volatile|required|const|new)\b")) {
        return $false
    }

    return $trimmedLine.EndsWith(";")
}

function Test-MethodDeclaration {
    param(
        [string]$LineText
    )

    $trimmedLine = $LineText.Trim()
    if ([string]::IsNullOrWhiteSpace($trimmedLine)) {
        return $false
    }

    if ($trimmedLine.StartsWith("[", [System.StringComparison]::Ordinal)) {
        return $false
    }

    if ($trimmedLine -cmatch "^(if|for|foreach|while|switch|catch|lock|using|return|throw)\b") {
        return $false
    }

    if ($trimmedLine -notmatch "\(") {
        return $false
    }

    if ($trimmedLine -match "=\s*[^;]*\(") {
        return $false
    }

    if ($trimmedLine -cmatch "\b(delegate|record|class|interface|struct|enum)\b") {
        return $false
    }

    $hasAccessModifier = $trimmedLine -cmatch "^(public|protected|internal|private|file|static|virtual|override|abstract|sealed|async|extern|unsafe|new)\b"
    $isInterfaceStyleSignature = $trimmedLine -match "^[A-Za-z_][\w<>\[\],?\s]*\s+[A-Za-z_]\w*\s*\([^=]*\)\s*;$"

    if (($hasAccessModifier -or $isInterfaceStyleSignature) -and $trimmedLine -match "\)\s*(\{|=>|;)") {
        return $true
    }

    if ($hasAccessModifier -and $trimmedLine.EndsWith("(", [System.StringComparison]::Ordinal)) {
        return $true
    }

    if ($hasAccessModifier -and $trimmedLine -match "\)\s*where\s") {
        return $true
    }

    return $false
}

function Get-CommentLines {
    param(
        [string]$IndentText,
        [string]$SummaryText
    )

    return @(
        "$IndentText/// <summary>",
        "$IndentText/// $SummaryText",
        "$IndentText/// </summary>"
    )
}

function Get-CommentSummaryText {
    param(
        [string]$DeclarationKind
    )

    $typeSummary = Get-TextFromCodes -Codes @(0x5B9A, 0x4E49, 0x5F53, 0x524D, 0x7C7B, 0x578B, 0x3002)
    $propertySummary = Get-TextFromCodes -Codes @(0x83B7, 0x53D6, 0x6216, 0x8BBE, 0x7F6E, 0x5F53, 0x524D, 0x5C5E, 0x6027, 0x503C, 0x3002)
    $fieldSummary = Get-TextFromCodes -Codes @(0x5B58, 0x50A8, 0x5F53, 0x524D, 0x5B57, 0x6BB5, 0x503C, 0x3002)
    $methodSummary = Get-TextFromCodes -Codes @(0x6267, 0x884C, 0x5F53, 0x524D, 0x65B9, 0x6CD5, 0x3002)
    $memberSummary = Get-TextFromCodes -Codes @(0x5B9A, 0x4E49, 0x5F53, 0x524D, 0x6210, 0x5458, 0x3002)

    switch ($DeclarationKind) {
        "Type" { return $typeSummary }
        "Property" { return $propertySummary }
        "Field" { return $fieldSummary }
        "Method" { return $methodSummary }
        default { return $memberSummary }
    }
}

function Add-DeclarationComments {
    param(
        [string[]]$Lines
    )

    $result = New-Object System.Collections.Generic.List[string]
    $pendingAttributes = New-Object System.Collections.Generic.List[string]
    $braceDepth = 0

    foreach ($line in $Lines) {
        $trimmedLine = $line.Trim()
        $declarationKind = $null

        if ($braceDepth -le 2) {
            if (Test-TypeDeclaration -LineText $line) {
                $declarationKind = "Type"
            }
            elseif (Test-PropertyDeclaration -LineText $line) {
                $declarationKind = "Property"
            }
            elseif (Test-MethodDeclaration -LineText $line) {
                $declarationKind = "Method"
            }
            elseif (Test-FieldDeclaration -LineText $line) {
                $declarationKind = "Field"
            }
        }

        if ($trimmedLine.StartsWith("[", [System.StringComparison]::Ordinal) -and $braceDepth -le 2) {
            $pendingAttributes.Add($line)
            $braceDepth += Get-BraceDelta -LineText $line
            continue
        }

        if ($null -ne $declarationKind) {
            $indentSourceLine = $line
            if ($pendingAttributes.Count -gt 0) {
                $indentSourceLine = $pendingAttributes[0]
            }

            $indentText = Get-LeadingWhitespace -LineText $indentSourceLine
            $summaryText = Get-CommentSummaryText -DeclarationKind $declarationKind
            foreach ($commentLine in (Get-CommentLines -IndentText $indentText -SummaryText $summaryText)) {
                $result.Add($commentLine)
            }

            foreach ($attributeLine in $pendingAttributes) {
                $result.Add($attributeLine)
            }

            $pendingAttributes.Clear()
        }
        else {
            foreach ($attributeLine in $pendingAttributes) {
                $result.Add($attributeLine)
            }

            $pendingAttributes.Clear()
        }

        $result.Add($line)
        $braceDepth += Get-BraceDelta -LineText $line
    }

    foreach ($attributeLine in $pendingAttributes) {
        $result.Add($attributeLine)
    }

    return $result
}

function Add-MethodStepComments {
    param(
        [string[]]$Lines
    )

    $result = New-Object System.Collections.Generic.List[string]
    $braceDepth = 0
    $pendingMethod = $false
    $stepComment = Get-TextFromCodes -Codes @(0x6B65, 0x9AA4, 0xFF1A, 0x6309, 0x65E2, 0x5B9A, 0x6D41, 0x7A0B, 0x6267, 0x884C, 0x5F53, 0x524D, 0x65B9, 0x6CD5, 0x903B, 0x8F91, 0x3002)

    for ($index = 0; $index -lt $Lines.Length; $index++) {
        $line = $Lines[$index]
        $trimmedLine = $line.Trim()

        if ($braceDepth -le 2 -and (Test-MethodDeclaration -LineText $line)) {
            if ($trimmedLine -notmatch "=>" -and $trimmedLine -notmatch ";\s*$") {
                $pendingMethod = $true
            }
        }

        $result.Add($line)

        if ($pendingMethod -and $line.Contains("{")) {
            $indentText = (Get-LeadingWhitespace -LineText $line) + "    "
            $nextIndex = $index + 1
            $hasCommentAlready = $false

            if ($nextIndex -lt $Lines.Length) {
                $nextTrimmedLine = $Lines[$nextIndex].Trim()
                if ($nextTrimmedLine.StartsWith("//", [System.StringComparison]::Ordinal) -or $nextTrimmedLine.StartsWith("///", [System.StringComparison]::Ordinal)) {
                    $hasCommentAlready = $true
                }
            }

            if (-not $hasCommentAlready) {
                $result.Add("$indentText// $stepComment")
            }

            $pendingMethod = $false
        }

        $braceDepth += Get-BraceDelta -LineText $line

        if ($trimmedLine.EndsWith(";")) {
            $pendingMethod = $false
        }
    }

    return $result
}

function Repair-SourceFile {
    param(
        [string]$FilePath
    )

    $originalText = Read-FileTextUtf8 -FilePath $FilePath
    $normalizedText = $originalText -replace "`r`n", "`n"
    $lines = [regex]::Split($normalizedText, "`n")

    $withoutComments = @(Remove-FullLineComments -Lines $lines)
    $withDeclarationComments = @(Add-DeclarationComments -Lines $withoutComments)
    $withMethodStepComments = @(Add-MethodStepComments -Lines $withDeclarationComments)

    $resultText = [string]::Join("`r`n", $withMethodStepComments)
    if ($normalizedText.EndsWith("`n") -and -not $resultText.EndsWith("`r`n")) {
        $resultText += "`r`n"
    }

    if ($originalText -ne $resultText) {
        Write-FileTextUtf8 -FilePath $FilePath -FileText $resultText
    }
}

$sourceFiles = @(Get-SourceFiles -RootPath $RootDirectory)

foreach ($sourceFile in $sourceFiles) {
    Repair-SourceFile -FilePath $sourceFile
}
